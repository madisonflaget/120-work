//declare setup function
function setup() {
    //create a 800x600px canvas
    createCanvas(800,600);

    //set the color as 'teal'
    background('teal');
//end function
}

//declare draw function
function draw() {
    // tell it to write the following text "Hello World! I am Madison"
    // position text 30 px in and 50 px down
    text( "Hello World! I am Madison", 30, 50 );
//end function
}
